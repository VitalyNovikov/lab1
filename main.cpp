#include <iostream>
using namespace std;

int main() {
    // Begin1: Дано сторону квадрата a. Знайти його периметр P = 4·a.
    cout << "Задача Begin1: Обчислення периметра квадрата" << endl;  // Виведення заголовка задачі
    double a, P;  // Оголошення змінних для сторони квадрата та периметра
    cout << "Введіть сторону квадрата a: ";  // Запит на введення сторони квадрата
    cin >> a;  // Зчитування значення сторони квадрата з консолі
    P = 4 * a;  // Обчислення периметра квадрата
    cout << "Периметр квадрата: " << P << endl;  // Виведення периметра на екран

    // Begin5: Дано довжину ребра куба a. Знайти об'єм куба V = a³ і площу його поверхні S = 6·a².
    cout << " " << endl;
    cout << "Задача Begin5: Обчислення об'єму і площі поверхні куба" << endl;  // Виведення заголовка задачі
    double V, S;  // Оголошення змінних для об'єму куба і площі поверхні
    cout << "Введіть довжину ребра куба a: ";  // Запит на введення довжини ребра куба
    cin >> a;  // Зчитування значення довжини ребра куба з консолі
    V = a * a * a;  // Обчислення об'єму куба
    S = 6 * a * a;  // Обчислення площі поверхні куба
    cout << "Об'єм куба: " << V << endl;  // Виведення об'єму куба на екран
    cout << "Площа поверхні куба: " << S << endl;  // Виведення площі поверхні куба на екран

    // Begin10: Дано два кола з радіусами R1 і R2. Знайти площі цих кіл і площу кільця.
    cout << " " << endl;
    cout << "Задача Begin10: Обчислення площі кіл і кільця" << endl;  // Виведення заголовка задачі
    double R1, R2;  // Оголошення змінних для радіусів кіл
    const double pi = 3.14;  // Константа для числа π
    cout << "Введіть радіус першого кола R1: ";  // Запит на введення радіуса першого кола
    cin >> R1;  // Зчитування значення радіуса першого кола з консолі
    cout << "Введіть радіус другого кола R2: ";  // Запит на введення радіуса другого кола
    cin >> R2;  // Зчитування значення радіуса другого кола з консолі
    double S1 = pi * R1 * R1;  // Обчислення площі першого кола
    double S2 = pi * R2 * R2;  // Обчислення площі другого кола
    double S3 = S1 - S2;  // Обчислення площі кільця
    cout << "Площа першого кола: " << S1 << endl;  // Виведення площі першого кола на екран
    cout << "Площа другого кола: " << S2 << endl;  // Виведення площі другого кола на екран
    cout << "Площа кільця: " << S3 << endl;  // Виведення площі кільця на екран

    // Begin15: Змінити значення A, B, C.
    cout << " " << endl;
    cout << "Задача Begin15: Заміна значень A, B, C" << endl;  // Виведення заголовка задачі
    double A, B, C;  // Оголошення змінних для трьох чисел
    cout << "Введіть три числа A, B, C: ";  // Запит на введення трьох чисел
    cin >> A >> B >> C;  // Зчитування значень A, B і C з консолі
    double temp = A;  // Збереження значення A у тимчасовій змінній
    A = C;  // Переміщення значення C в A
    C = B;  // Переміщення значення B в C
    B = temp;  // Переміщення значення A (яке зберігається в temp) в B
    cout << "Нове значення A: " << A << endl;  // Виведення нового значення A на екран
    cout << "Нове значення B: " << B << endl;  // Виведення нового значення B на екран
    cout << "Нове значення C: " << C << endl;  // Виведення нового значення C на екран

    // Begin20: Дано число A. Обчислити A¹⁵, використовуючи п'ять операцій множення.
    cout << " " << endl;
    cout << "Задача Begin20: Обчислення A¹⁵" << endl;  // Виведення заголовка задачі
    double A2, A3, A5, A10, A15;  // Оголошення змінних для різних степенів числа
    cout << "Введіть число A: ";  // Запит на введення числа A
    cin >> A;  // Зчитування значення A з консолі
    A2 = A * A;  // Обчислення A²
    A3 = A2 * A;  // Обчислення A³
    A5 = A3 * A2;  // Обчислення A⁵
    A10 = A5 * A5;  // Обчислення A¹⁰
    A15 = A10 * A5;  // Обчислення A¹⁵
    cout << "A²: " << A2 << endl;  // Виведення A² на екран
    cout << "A³: " << A3 << endl;  // Виведення A³ на екран
    cout << "A⁵: " << A5 << endl;  // Виведення A⁵ на екран
    cout << "A¹⁰: " << A10 << endl;  // Виведення A¹⁰ на екран
    cout << "A¹⁵: " << A15 << endl;  // Виведення A¹⁵ на екран

    // Begin25: Вартість цукерок.
    cout << " " << endl;
    cout << "Задача Begin25: Вартість цукерок" << endl;  // Виведення заголовка задачі
    double X, Y, cost_per_kg, total_cost;  // Оголошення змінних для маси, ціни та вартості
    cout << "Введіть масу X кг та ціну A гривень: ";  // Запит на введення маси і ціни
    cin >> X >> a;  // Зчитування маси X і ціни A з консолі
    cost_per_kg = a / X;  // Обчислення вартості 1 кг цукерок
    cout << "Вартість 1 кг: " << cost_per_kg << endl;  // Виведення вартості 1 кг цукерок на екран
    cout << "Введіть масу Y кг: ";  // Запит на введення маси Y кг
    cin >> Y;  // Зчитування маси Y з консолі
    total_cost = cost_per_kg * Y;  // Обчислення вартості Y кг цукерок
    cout << "Вартість " << Y << " кг: " << total_cost << endl;  // Виведення вартості Y кг цукерок на екран

    // Begin30: Розв'язок лінійного рівняння Ax + B = 0.
    cout << " " << endl;
    cout << "Задача Begin30: Лінійне рівняння" << endl;  // Виведення заголовка задачі
    cout << "Введіть коефіцієнти A та B для рівняння Ax + B = 0: ";  // Запит на введення коефіцієнтів
    cin >> A >> B;  // Зчитування коефіцієнтів A і B з консолі
    if (A != 0) {  // Перевірка, чи не дорівнює A нулю
        double x = -B / A;  // Обчислення розв'язку рівняння
        cout << "Розв'язок рівняння: x = " << x << endl;  // Виведення розв'язку на екран
    } else {
        cout << "Коефіцієнт A не може дорівнювати нулю!" << endl;  // Повідомлення про помилку, якщо A дорівнює нулю
    }

    return 0;  // Завершення програми
}
